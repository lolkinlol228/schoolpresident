function animateCounterToValue(element, from, to) {
  const duration = 1000;
  const increment = (to - from) / (duration / 30);
  let current = from;
  
  const timer = setInterval(() => {
    current += increment;
    if (current >= to) {
      current = to;
      clearInterval(timer);
    }
    element.textContent = Math.floor(current);
  }, 30);
}

